package com.jasonhatfield.homeschoolinteractive.model;

/**
 * The AssignmentStatus enum represents the different statuses that an assignment can have.
 * It includes the following statuses:
 * - Incomplete: Indicates that the assignment is not yet completed.
 * - Review: Indicates that the assignment is under review.
 * - Accepted: Indicates that the assignment has been accepted.
 */
public enum AssignmentStatus {
    Incomplete, Review, Accepted
}
